                <h1 class="title-font sm:text-4xl text-3xl mb-4 font-medium text-gray-900">డాక్టర్ కేశవరావు బలిరామ్ హెడ్గెవార్ (డాక్టర్‌జీ) </h1>
                <p class="-mb-8 leading-relaxed">డాక్టర్ కేశవరావ్ బలీరాం హెడ్గెవార్ రాష్ట్రీయ స్వయం సేవక సంఘాన్ని స్థాపించిన మహాపురుషులు. వీరు క్రీ.శ 1889 వ సంవత్సరం ఉగాది పర్వదినాన జన్మించారు.

వీరి తల్లిదండ్రులు రేవతీబాయి, బలిరాంపంత్ అనే పుణ్యదంపతులు. కేశవరావు జన్మజాత దేశభక్తులు. 12 సంవత్సరాల వయస్సులో విక్టోరియా రాణి సింహాసనాన్ని అధిష్ఠించి 60 సంవత్సరాలు పూర్తయిన సందర్భంగా పంచి పెట్టిన మిఠాయిలను విసిరి ఆవల పారేశాడు.

1908లో నీల్ సిటీ హైస్కూలులో చదువుచున్న రోజుల్లో పాఠశాలకు పరిశీలనాధికారి వచ్చినప్పుడు వందేమాతరం ఉద్యమాన్ని నిర్వహించాడు. పాఠశాల బహిష్కరింపబడ్డారు. పూనావెళ్ళి మెట్రిక్యులేషన్ పూర్తిచేశారు. తిలక్ మహాశయుని అజ్ఞానుసారం కోల్ కతాలో వైద్యకళాశాలలో చేరి అనుశీలన సమితి అనే విప్లవసంస్థలో చేరి దేశ స్వాతంత్ర్యం కోసం పోరాడారు. వైద్యవిద్యను పూర్తిచేసుకొని తన డిగ్రీలను ఉదర పోషణకు, ధనసంపాదనకు వినియోగించకుండా శేషజీవితాన్ని భారతమాత సేవలో సమర్పించాలని నిర్ణయించుకొని బ్రహ్మచర్యాన్ని స్వీకరించి లోకమాన్యుని ఆదేశానుసారం అఖిల భారత కాంగ్రెసులో చేరి స్వాతంత్ర్య ఉద్యమంలో ప్రముఖపాత్ర వహించారు. కొంతకాలం జైలు జీవితాన్ని గడిపారు. కాంగ్రెస్ సంస్థలో అనేక బాధ్యతలు తీసుకుని పనిచేశారు. హిందూ మహాసభలో చేరి కొంతకాలం పనిచేశారు.

ఆ అనుభవంతోనే వేయి సంవత్సరాలుగా పిందూ రాష్ట్ర పరాజయానికి కారణాలను విశ్లేషించారు. హిందూ సంఘటన ఆవశ్యకతను గుర్తించి 1925 విజయదశమి పర్వదినాన నాగపూరు పట్టణంలో రాష్ట్రీయ స్వయంసేవక సంఘాన్ని స్థాపించారు. సంఘటన కార్యం ప్రారంభించడంతోనే సహజసిద్ధంగా ఉన్న కోపస్వభావాన్ని విడిచిపెట్టి “స్వభావో దురతిక్రమః” అనే లోకోక్తిని వమ్ముచేశారు.

డాక్టర్ హెడ్గెవార్ కు ఈ రాష్ట్ర జీవనంలోని హిందువులలో హిందుత్వ భావనను జాగృతపరచి తద్వారా హిందూసమాజ సంఘటన చేసి దాని ఆధారంగా ఈ రాష్ట్రాన్ని పరమవైభవ స్థితికి తీసుకు వెళ్ళాలి అనేది జీవిత ధ్యేయంగా ఉండేది. కుల, ప్రాంత, భాష వర్గాభిమానాలకతీతంగా హిందూ ధర్మం, హిందూ సంస్కృతి, మరియు హిందూ దేశానికి మేలు కలుగుతుంది. అదే సమాజానికి కళ్యాణకారకమవుతుందని ఆలోచన చేశారు. ఈ ఆలోచనకు సాకార రూపమే రాష్ట్రీయ స్వయం సేవక సంఘము. ఆత్మ ప్రేరణతో, మొక్కవోని ఆత్మవిశ్వాసంతో అత్యంత సరళము మరియు సహజసిద్ధమునైన పద్ధతిలో హిందూ సంఘటన జరగడానికి పూజనీయ డాక్టర్జీ చూపించిన కార్యపద్ధతి వారి మేధాశక్తికి తార్కాణము. ఈ కార్యంలో వారి ప్రతిభాపాటవాలను సమర్పించుకున్న యుగద్రష్ట డాక్టర్ హెడ్గేవార్.

డాక్టర్ హెడ్గేవార్ పూర్వీకులు వేదపండితులు. మీరు ఆంధ్ర ప్రాంతంలోని ఇందూరు (నిజామాబాద్) జిల్లాలోని కందకుర్తి అనే గ్రామం నుండి నాగపూరు వలస వచ్చినట్లు చరిత్ర చెప్తోంది. 21 జూన్ 1940లో డాక్టర్జీ పరమపదించారు. డాక్టర్టీ తీవ్ర తపశ్చర్య కారణంగా సంఘం ఇంతింతగా పెరుగుతూ నేడు దేశానికి ఒక ఆశాకేంద్రంగా వెలుగొందుతున్నది.

